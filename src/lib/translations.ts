const PLACEHOLDER_ES: Array<[RegExp, string]> = [
  [/^Group ([A-Z0-9]+) Winner$/,    'Ganador Grupo $1'],
  [/^Group ([A-Z0-9]+) 2nd Place$/, '2° Grupo $1'],
  [/^Quarterfinal (\d+) Winner$/,   'Ganador Cuartos $1'],
  [/^Round of 32 (\d+) Winner$/,    'Ganador R32 #$1'],
  [/^Round of 16 (\d+) Winner$/,    'Ganador Octavos $1'],
  [/^Semifinal (\d+) Winner$/,      'Ganador Semifinal $1'],
  [/^Semifinal (\d+) Loser$/,       'Perdedor Semifinal $1'],
  [/^Third Place (.+)$/,            'Tercero $1'],
]

export function getTeamName(name: string, language: string): string {
  if (language !== 'es') return name
  if (TEAM_ES[name]) return TEAM_ES[name]
  for (const [re, tpl] of PLACEHOLDER_ES) {
    if (re.test(name)) return name.replace(re, tpl)
  }
  return name
}
